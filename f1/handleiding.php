<h1> Beste gebruiker, </h1><br>
Fijn dat u gebruik wilt maken van ons systeem over de Formule 1. Hier leest u de instructies voor het gebruik van ons systeem.<br><br><br>

<h2>Hoofdmenu</h2><br>

<li>Als u op de cirkel links bovenin het scherm klikt, dan verschijnt het menu.</li><br>

<li>Vanaf daar heeft u de keuze om naar de menu’s van de onderwerpen te gaan, om in te loggen, een gebruiker toe te voegen en om uit te loggen.</li><br>

<li>In de menu's van de onderwerpen kunt u een overzicht vinden en gegevens verwijderen, aanpassen en toevoegen.</li><br><br>

<h2>Gebruiker toevoegen/ invoegen</h2><br>

<li>Open het hoofdmenu, klik dan op ‘Gebruiker invoegen'.</li><br>

<li>U kunt hier een gebruikersnaam en wachtwoord aanmaken om later in te loggen.</li><br><br>

<h2>Inloggen</h2><br>

<li>Open het hoofdmenu, klik dan op ‘Inloggen'.</li><br>

<li>Wanneer u op ‘Inloggen' klikt dan kunt u inloggen met het account dat u heeft aangemaakt.*</li><br><br>

<h2>Uitloggen</h2><br>

<li>Hier kunt u uitloggen uit uw account.</li><br>

<li>Als u wilt uitloggen opent u het hoofdmenu, en klikt dan op ‘Uitloggen'.</li><br>

<li>Let op: Wanneer u uitlogt kunt u niks meer toevoegen of aanpassen.</li><br><br>

<h2>Toevoegen</h2><br>

<li>U dient eerst in te loggen voordat u iets kunt toevoegen.**</li><br>

<li>Als u heeft ingelogd, dan kunt u het hoofdmenu weer openen en op het onderwerp waar u iets aan wilt toevoegen klikken.</li><br>

<li>Daarna kunt u, als u op de knop heeft geklikt, op het stukje ‘toevoegen’ klikken.</li><br>

<li>Daarna vult u alle kolommen (zo goed mogelijk) in (indien er bijvoorbeeld geen startnummer is, dan laat u die leeg).<br>&nbsp;&nbsp;&nbsp;&nbsp; 
 Een coureur_id, circuit_id en team_id hoeft u niet in te vullen dat gaat automatisch.</li><br>

<li>Als laatste klikt u op de groene knop en dan heeft u hetgeen toegevoegd dat u wilt toevoegen, bijvoorbeeld een coureur dus. </li><br><br>

<h2>Overzichten</h2><br>

<li>Open het hoofdmenu, en klik op het onderwerp waarvan u het overzicht wilt zien.</li><br>

<li>Als u het onderwerp heeft aangeklikt, dan klikt u op het tabje dat ‘overzichten’ heet en dan heeft u het overzicht geopend.</li><br><br>

<h2>Aanpassen/ verwijderen</h2><br>

<li>U dient eerst in te loggen voordat u iets kunt aanpassen of verwijderen.**</li><br>

<li>Als u heeft ingelogd, dan kunt u het hoofdmenu weer openen en op het onderwerp waar u iets aan wilt aanpassen of iets van wilt verwijderen klikken.</li><br>

<li>Daarna kunt u, als u op de knop heeft geklikt, op het stukje ‘aanpassen’ klikken.</li><br>

<li>Als u op de knop heeft gedrukt, dan zoekt u de rij die u wilt aanpassen/verwijderen en klikt u op de knop ‘aanpassen’ of ‘verwijderen’.</li><br>

<li>Als u gaat aanpassen, dan past u het vakje aan dat u wilt aanpassen en dan klikt u op de groene knop.</li><br>

<li>Als u gaat verwijderen, dan is het klikken op de knop 'verwijderen’ voldoende.</li><br><br>

<h2>Zoeken</h2><br>

<li>Open het hoofdmenu, en klik dan op zoeken als u uitgebreid wilt zoeken. En klik op een onderwerp als u eenvoudig wilt zoeken.</li><br>

<li>Let op: nadat u een zoekopdracht heeft uitgevoerd gaat de keuze uit het optiemenu weer terug naar de default als u uitgebreid zoekt.</li><br>

<li>Wanneer u gaat zoeken in een onderwerp, klik dan op ‘zoeken’ en voer in waar u op wilt zoeken.</li><br><br>

<h6>* voor meer info over een account aanmaken kunt u het stukje over gebruiker toevoegen/ invoegen raadplegen.</h6>

<h6>** voor meer info over inloggen kunt u het stukje over inloggen raadplegen.</h6>

